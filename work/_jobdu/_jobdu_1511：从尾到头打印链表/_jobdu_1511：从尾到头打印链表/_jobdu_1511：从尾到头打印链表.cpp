/************************************************************************/
/* 题目描述：
输入一个链表，从尾到头打印链表每个节点的值。
输入：
每个输入文件仅包含一组测试样例。
每一组测试案例包含多行，每行一个大于0的整数，代表一个链表的节点。第一行是链表第一个节点的值，依次类推。当输入到-1时代表链表输入完毕。-1本身不属于链表。
输出：
对应每个测试案例，以从尾到头的顺序输出链表每个节点的值，每个值占一行。
样例输入：
1
2
3
4
5
-1
样例输出：
5
4
3
2
1                                                                     */
/************************************************************************/
#include <stdio.h>
#include <stdlib.h>
typedef struct node
{
	int   m_value;
	node* next;
}NODE;
int main()
{
	NODE * pHead = new NODE;
	pHead->next = NULL;
	NODE * pWork = NULL;
	int  g_mIntValue = -1;
	while(scanf("%d",&g_mIntValue) != EOF)
	{
		if(-1 == g_mIntValue)
			break;
		pWork = new NODE;
		pWork->next = pHead->next;
		pHead->next = pWork;
		pWork->m_value = g_mIntValue ;		
	}
	pWork = pHead->next;
	while(pWork)
	{
		printf("%d \n",pWork->m_value);
		pWork = pWork->next;
	}
	pWork = pHead->next;
	while(pWork)
	{
		pHead->next = pWork->next;
		pWork->next = NULL;
		free(pWork);
		pWork = pHead->next;
	}
	free(pHead);
	pHead = pWork = NULL;
	return 1;
}